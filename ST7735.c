/* ------------------------ 底层 SPI 发送 (极速优化版) ------------------------ */
static void SPI_WriteByte(uint8_t data) {
    for (uint8_t i = 0; i < 8; i++) {
        TFT_SCL(0);                     // 时钟拉低
        if (data & 0x80)
            TFT_SDA(1);
        else
            TFT_SDA(0);
        data <<= 1;
        // ⚠️ 删除 Delay_us：168MHz下GPIO翻转约6ns，ST7735完全支持极速
        TFT_SCL(1);                     // 时钟上升沿，屏幕锁存数据
    }
    TFT_SCL(0);                         // 空闲状态时钟低
}

/* ------------------------ 基本读写命令 ------------------------ */
static void ST7735_WriteCmd(uint8_t cmd) {
    TFT_CS(0);
    TFT_DC(0);
    SPI_WriteByte(cmd);
    TFT_CS(1);
}

static void ST7735_WriteData(uint8_t data) {
    TFT_CS(0);
    TFT_DC(1);
    SPI_WriteByte(data);
    TFT_CS(1);
}

static void ST7735_WriteData16(uint16_t data) {
    TFT_CS(0);
    TFT_DC(1);
    SPI_WriteByte(data >> 8);
    SPI_WriteByte(data & 0xFF);
    TFT_CS(1);
}

/* ------------------------ GPIO 初始化（F4 标准库） ------------------------ */
static void ST7735_GPIO_Init(void) {
    RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOD | RCC_AHB1Periph_GPIOE, ENABLE);

    GPIO_InitTypeDef gpio;
    gpio.GPIO_Mode = GPIO_Mode_OUT;
    gpio.GPIO_OType = GPIO_OType_PP;
    gpio.GPIO_Speed = GPIO_Speed_100MHz;
    gpio.GPIO_PuPd = GPIO_PuPd_NOPULL;

    // PD2(SCL), PD3(SDA), PD4(CS)
    gpio.GPIO_Pin = TFT_SCL_PIN | TFT_SDA_PIN | TFT_CS_PIN;
    GPIO_Init(TFT_SCL_PORT, &gpio);

    // PE1(DC), PE2(RST)
    gpio.GPIO_Pin = TFT_DC_PIN | TFT_RST_PIN;
    GPIO_Init(TFT_DC_PORT, &gpio);

    TFT_CS(1);
    TFT_SCL(0);
}

/* ------------------------ 硬件复位 ------------------------ */
static void ST7735_Reset(void) {
    TFT_RST(0);
    Delay_ms(50);
    TFT_RST(1);
    Delay_ms(100);
}

/* ------------------------ 初始化序列 ------------------------ */
void ST7735_Init(void) {
    ST7735_GPIO_Init();
    ST7735_Reset();

    ST7735_WriteCmd(0x11); // Sleep out
    Delay_ms(120);

    ST7735_WriteCmd(0xB1); // Frame rate control
    ST7735_WriteData(0x01);
    ST7735_WriteData(0x2C);
    ST7735_WriteData(0x2D);

    ST7735_WriteCmd(0xB2);
    ST7735_WriteData(0x01);
    ST7735_WriteData(0x2C);
    ST7735_WriteData(0x2D);

    ST7735_WriteCmd(0xB3);
    ST7735_WriteData(0x01);
    ST7735_WriteData(0x2C);
    ST7735_WriteData(0x2D);
    ST7735_WriteData(0x01);
    ST7735_WriteData(0x2C);
    ST7735_WriteData(0x2D);

    ST7735_WriteCmd(0xB4);
    ST7735_WriteData(0x07);

    ST7735_WriteCmd(0xC0);
    ST7735_WriteData(0xA2);
    ST7735_WriteData(0x02);
    ST7735_WriteData(0x84);
    ST7735_WriteCmd(0xC1);
    ST7735_WriteData(0xC5);

    ST7735_WriteCmd(0x36); // MADCTL
    ST7735_WriteData(0xA0); // 横屏方向

    ST7735_WriteCmd(0x3A); // 16-bit RGB565
    ST7735_WriteData(0x05);

    ST7735_WriteCmd(0x2A); // 列地址 0~127
    ST7735_WriteData(0x00);
    ST7735_WriteData(0x00);
    ST7735_WriteData(0x00);
    ST7735_WriteData(0x7F);

    ST7735_WriteCmd(0x2B); // 行地址 0~159
    ST7735_WriteData(0x00);
    ST7735_WriteData(0x00);
    ST7735_WriteData(0x00);
    ST7735_WriteData(0x9F);

    ST7735_WriteCmd(0x29); // Display on
}

/* ------------------------ 窗口与绘制 ------------------------ */
void ST7735_SetWindow(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2) {
    ST7735_WriteCmd(0x2A);
    ST7735_WriteData16(x1);
    ST7735_WriteData16(x2);

    ST7735_WriteCmd(0x2B);
    ST7735_WriteData16(y1);
    ST7735_WriteData16(y2);

    ST7735_WriteCmd(0x2C);
}

void ST7735_WriteColor(uint16_t color, uint16_t len) {
    TFT_CS(0);
    TFT_DC(1);
    while (len--) {
        SPI_WriteByte(color >> 8);
        SPI_WriteByte(color & 0xFF);
    }
    TFT_CS(1);
}

void ST7735_FillRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color) {
    ST7735_SetWindow(x, y, x + w - 1, y + h - 1);
    ST7735_WriteColor(color, (uint32_t)w * h);
}

void ST7735_DrawPixel(uint16_t x, uint16_t y, uint16_t color) {
    ST7735_SetWindow(x, y, x, y);
    ST7735_WriteColor(color, 1);
}

void ST7735_DrawImage(uint16_t x, uint16_t y, uint16_t w, uint16_t h, const uint16_t *data) {
    ST7735_SetWindow(x, y, x + w - 1, y + h - 1);
    TFT_CS(0);
    TFT_DC(1);
    for (uint32_t i = 0; i < (uint32_t)w * h; i++) {
        uint16_t color = *data++;
        SPI_WriteByte(color >> 8);
        SPI_WriteByte(color & 0xFF);
    }
    TFT_CS(1);
}

/* ------------------------ 摄像头逐行显示 (极速优化版) ------------------------ */
void ST7735_DrawLineImage(uint16_t y, const uint8_t *lineBuf) {
    uint16_t i;
    ST7735_SetWindow(0, y, IMAGE_WIDTH - 1, y); // 设置窗口
    
    TFT_CS(0);  // 整行只拉低一次 CS
    TFT_DC(1);  // 数据模式
    
    // 内联展开软件SPI，去掉所有 Delay_us，168MHz下ST7735完全能跟上
    for (i = 0; i < LINE_SIZE; i++) {
        uint8_t data = lineBuf[i];
        for (uint8_t bit = 0; bit < 8; bit++) {
            TFT_SCL(0);
            if (data & 0x80) TFT_SDA(1);
            else TFT_SDA(0);
            data <<= 1;
            TFT_SCL(1); // 上升沿锁存
        }
    }
    TFT_SCL(0);
    TFT_CS(1);  // 整行发完才拉高 CS
}

void ST7735_DrawString_8x8(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bg) {
    while (*str) {
        ST7735_DrawChar_8x8(x, y, *str++, color, bg);
        x += 8;
        if (x + 8 > IMAGE_WIDTH) break;   // 横屏宽度 160
    }
}

/* ------------------------ 字符显示 ------------------------ */
void ST7735_DrawChar_8x8(uint16_t x, uint16_t y, char ch, uint16_t color, uint16_t bg) {
    if (ch < 32 || ch > 126) ch = 32;
    const uint8_t *p = Font8x8[ch - 32];

    for (uint8_t row = 0; row < 8; row++) {
        uint8_t byte = p[row];
        for (uint8_t col = 0; col < 8; col++) {
            uint8_t bit = (byte >> col) & 0x01;
            if (bit)
                ST7735_DrawPixel(x + col, y + row, color);
            else
                ST7735_DrawPixel(x + col, y + row, bg);
        }
    }
}

void ST7735_PutString(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bg_color) {
    ST7735_DrawString_8x8(x, y, str, color, bg_color);
}
